class RequiredDocumentsHelper {
  static List<String> getRequiredDocuments(String? vehicleType, String? fuelType) {
    if (vehicleType == null) return [];

    switch (vehicleType) {
      case 'خصوصي':
      case 'عمومي':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'بطاقة الهوية الشخصية',
        ];

      case 'شحن خفيف':
      case 'شحن ثقيل':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'بطاقة الهوية الشخصية',
          'شهادة فحص صلاحية للمعدات الهيدروليكية (إن وجدت)',
        ];

      case 'مركبة نقل وقود/مواد خطرة':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'بطاقة الهوية الشخصية',
          'شهادة فحص صلاحية',
          'موافقة الهيئة العامة للبترول',
          'موافقة الدفاع المدني',
        ];

      case 'حافلة نقل عام أو خاصة':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'بطاقة الهوية الشخصية',
          'رخصة تشغيل (بيرمت) سارية المفعول',
        ];

      case 'مركبة مدارس':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'بطاقة الهوية الشخصية',
          'رخصة الروضة أو المدرسة سارية المفعول',
          'رخصة تشغيل (بيرمت)',
        ];

      case 'مركبة حكومية':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'موافقة النقل الحكومي',
        ];

      case 'مركبة تأجير':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'رخصة شركة التأجير سارية المفعول',
        ];

      case 'صهريج نضح أو مياه أو حليب':
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'موافقة الصحة والبيئة أو وزارة الصحة',
        ];

      default:
        return [
          'رخصة المركبة الأصلية أو بدل فاقد',
          'تأمين ساري المفعول',
          'بطاقة الهوية الشخصية',
        ];
    }
  }
}
